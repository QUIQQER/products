define('package/quiqqer/products/bin/controls/fields/types/Select', [

    'qui/QUI',
    'qui/controls/Control'

], function (QUI, QUIControl) {
    "use strict";

    return new Class({
        Extends: QUIControl,
        Type   : 'package/quiqqer/products/bin/controls/fields/types/Select',

        Binds: [
            '$onImport'
        ],

        initialize: function (options) {
            this.parent(options);

            this.addEvents({
                onImport: this.$onImport
            });
        },

        /**
         * event : on import
         */
        $onImport: function () {
            var Elm = this.getElm();

            new Element('select', {
                'class': 'field-container-field',
                name   : Elm.get('data-name')
            }).inject(Elm, 'after');

            Elm.destroy();
        }
    });
});
